tup = (4, 7, 8, 4, 3,11, 8)  # tuple is immutable(cannot change) unlike list which is mutable(can change--elements added\removed..etc)
print(tup)
print(tup[1])
print(tup.count(7))   # gives number of occurences of an element
print(tup.index)    #gives address of starting element of tuple
print(tup.index(11))    #gives location or index of given element
print(len(tup))    #length of tuple
